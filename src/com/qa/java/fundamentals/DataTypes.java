package com.qa.java.fundamentals;

public class DataTypes {

	public static void main(String[] args) {

		byte age; // age is a variable (address)
		age = 25;
		System.out.println("Age is :" + age); //Age is : 25
		
		age = 50;
		System.out.println("Age is :" + age); 
		
		short salary;
		salary = 30000;
		
		int productPrice;
		productPrice = 100;
		
		int totalSalary;
		totalSalary = 3423423;
		
		long companyAnnualRevenue;
		companyAnnualRevenue = 45345345343433L;
		System.out.println("Company Revenue : " + companyAnnualRevenue);
		
		//Real(Fractional Numbers)
		//4 bytes
		float averageSalary;
		averageSalary = 23423.23F;
	
		//8 bytes
		double averageAnnualRevenue;
		averageAnnualRevenue = 23423432.234;
		
		char gender ;
		gender = 'M';
		
		boolean isExists;
		isExists = true;
		
	}

}
